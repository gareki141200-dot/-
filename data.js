/* =========================================================
   除外食材・アレルギー照合のための表記ゆれ対策
   「えび」「エビ」「海老」のように、ひらがな・カタカナ・漢字で
   表記が異なっていても同じ食材として確実に検出できるようにする。
   ========================================================= */

// カタカナ→ひらがなに変換し、前後の空白・大文字小文字の違いを吸収する
function normalizeForMatch(str){
  if(!str) return "";
  return String(str).trim().toLowerCase().replace(/[\u30a1-\u30f6]/g, ch =>
    String.fromCharCode(ch.charCodeAt(0) - 0x60)
  );
}

