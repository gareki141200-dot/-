// 登録された除外語(1件)を、同義語グループの表記もすべて含む形に展開する
function expandExclusionTerm(term){
  const normTerm = normalizeForMatch(term);
  const group = ALLERGEN_SYNONYM_GROUPS.find(g =>
    g.some(variant => normalizeForMatch(variant) === normTerm)
  );
  return group ? group : [term];
}

